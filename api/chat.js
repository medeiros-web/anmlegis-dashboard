async function callClaude(messages, key, model, res) {
  const upstream = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'x-api-key': key,
      'anthropic-version': '2023-06-01',
      'content-type': 'application/json',
    },
    body: JSON.stringify({
      model: model || 'claude-haiku-4-5-20251001',
      max_tokens: 1024,
      system: SYSTEM,
      messages,
      stream: true,
    }),
  });
  if (!upstream.ok) {
    const err = await upstream.text();
    return res.status(upstream.status).json({ error: err });
  }
  await streamReader(upstream, res,
    evt => evt.type === 'content_block_delta' ? evt.delta?.text : null);
}

async function callOpenAI(messages, key, model, res, baseURL) {
  const url = (baseURL || 'https://api.openai.com') + '/v1/chat/completions';
  const upstream = await fetch(url, {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${key}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      model: model || 'gpt-4o-mini',
      max_tokens: 1024,
      stream: true,
      messages: [
        { role: 'system', content: SYSTEM },
        ...messages,
      ],
    }),
  });
  if (!upstream.ok) {
    const err = await upstream.text();
    return res.status(upstream.status).json({ error: err });
  }
  await streamReader(upstream, res,
    evt => evt.choices?.[0]?.delta?.content || null);
}

async function callGemini(messages, key, model, res) {
  const mdl = model || 'gemini-2.0-flash';
  const url = `https://generativelanguage.googleapis.com/v1beta/models/${mdl}:streamGenerateContent?key=${key}&alt=sse`;

  const contents = messages.map(m => ({
    role: m.role === 'assistant' ? 'model' : 'user',
    parts: [{ text: m.content }],
  }));

  const upstream = await fetch(url, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      system_instruction: { parts: [{ text: SYSTEM }] },
      contents,
      generationConfig: { maxOutputTokens: 1024 },
    }),
  });
  if (!upstream.ok) {
    const err = await upstream.text();
    return res.status(upstream.status).json({ error: err });
  }
  await streamReader(upstream, res,
    evt => evt.candidates?.[0]?.content?.parts?.[0]?.text || null);
}

