module.exports = async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method Not Allowed' });

  const { message, history = [], provider = 'claude', apiKey: clientKey, model } = req.body || {};
  if (!message) return res.status(400).json({ error: 'Campo message ausente.' });

  const messages = [
    ...history.slice(-10).map(m => ({ role: m.role, content: String(m.content) })),
    { role: 'user', content: String(message) },
  ];

  res.setHeader('Content-Type', 'text/event-stream');
  res.setHeader('Cache-Control', 'no-cache');
  res.setHeader('X-Accel-Buffering', 'no');

  if (provider === 'claude') {
    const key = process.env.ANTHROPIC_API_KEY || clientKey;
    if (!key) return res.status(500).json({ error: 'Chave Claude não configurada.' });
    return callClaude(messages, key, model, res);
  }

  if (provider === 'chatgpt') {
    const key = clientKey || process.env.OPENAI_API_KEY;
    if (!key) return res.status(400).json({ error: 'Chave OpenAI não informada.' });
    return callOpenAI(messages, key, model, res, 'https://api.openai.com');
  }

  if (provider === 'gemini') {
    const key = clientKey || process.env.GEMINI_API_KEY;
    if (!key) return res.status(400).json({ error: 'Chave Gemini não informada.' });
    return callGemini(messages, key, model, res);
  }

  if (provider === 'grok') {
    const key = clientKey || process.env.GROK_API_KEY;
    if (!key) return res.status(400).json({ error: 'Chave Grok não informada.' });
    return callOpenAI(messages, key, model || 'grok-3-mini', res, 'https://api.x.ai');
  }

  return res.status(400).json({ error: `Provider desconhecido: ${provider}` });
};
